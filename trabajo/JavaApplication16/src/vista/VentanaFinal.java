    private void abrirLarguillo(int opcion) {
        vl = new VerLarguillo(null, false);

        switch (opcion) {
            case 0:
                vl.setDatos(this.cc.columnasLarguilloNormal);
                for (int i = 0; i < 100; i++) {
                    vl.addFila(this.cc.data[i].getVector());
                }
                break;
            case 1: case 2:  case 3: case 4:
                vl.setDatos(this.cc.columnasRespuestasEje);
                for (int i = 0; i < 100; i++) {
                    
                    if (opcion == 1) {
                        vl.addFila(this.cc.simpleRespuesta[i].getVector());
                    }
                    if (opcion == 2) {
                        vl.addFila(this.cc.dualRespuesta[i].getVector());
                    }
                    if (opcion == 3) {
                        vl.addFila(this.cc.tandemRespuesta[i].getVector());
                    }
                    if (opcion == 4) {
                        vl.addFila(this.cc.tridemRespuesta[i].getVector());
                    }
                }
                break;

            case 5://deformacion y fatiga 1
                
                vl.setDatos(this.cc.columnasRespuestasEje());
                for (int i = 0; i < 100; i++) {
                    vl.addFila(this.cc.nre[i].getVector());
                }
                
                break;
            case 6://espectros de repeticiones esperadas
                vl.setDatos(this.cc.colEspectroRepeticionesEsperadas);
                for (int i = 0; i < 100; i++) {
                    vl.addFila(this.cc.ere[i].getVector());
                }
                break;
            case 7://fatiga y deformacion 2
                vl.setDatos(this.cc.columnasRespuestasEje());
                for (int i = 0; i < 100; i++) {
                    vl.addFila(this.cc.ed[i].getVector());
                }
                break;
            case 8://deformacion todos
                vl.setDatos(this.cc.colsEspectroTotal);
                for (int i = 0; i < 100; i++) {
                    vl.addFila(this.cc.et[i].getVector());
                }
                break;
            case 9://espectro de daño acomulado
                vl.setDatos("Simple Def, Dual Def, Tandem Def, TRIDEM def, TODOS def,Simple fat, Dual fat, Tandem fat, TRIDEM fat, TODOS fat".split(","));
                for (int i = 0; i < 100; i++) {
                    vl.addFila(this.cc.eda[i].getVector());
                }
                break;
 

        }
        vl.setNoEditable();
        vl.setVisible(true);

    }

